use App\Http\Controllers\AuthController;
use App\Http\Controllers\UserController;

Route::post('register', [AuthController::class, 'register']);
Route::post('login', [AuthController::class, 'login']);

Route::middleware('auth:api')->group(function () {
    Route::get('users', [UserController::class, 'index']);
    Route::get('users/{id}', [UserController::class, 'show']);
    Route::put('users/{id}', [UserController::class, 'update']);
    Route::delete('users/{id}', [UserController::class, 'destroy']);
    Route::post('users/{id}/addresses', [UserController::class, 'assignAddress']);
    Route::put('users/{userId}/addresses/{addressId}', [UserController::class, 'updateAddress']);
    Route::delete('users/{userId}/addresses/{addressId}', [UserController::class, 'deleteAddress']);
});
